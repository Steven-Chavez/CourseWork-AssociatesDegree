    // performs the encoding of the message and displays the encoded
    // message back to the user
    private void btEncodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEncodeActionPerformed
        String msg = txtFieldMsg.getText();
        
        if(rbRandom.isSelected())
        {
            enigma.setMessage(msg);
        }
        else
        {
            int key = (int)cbxKey.getSelectedItem();
            enigma.setMessage(msg, key);
        }
        
        txtAreaResults.setText(enigma.getEncodedMessage());
        cbxKey.setSelectedItem(enigma.getKey()); 
    }//GEN-LAST:event_btEncodeActionPerformed

    // Saves encoded message and key used to encode to a file 
    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        createFilePath();
    }//GEN-LAST:event_btSaveActionPerformed

    // Opens an open file dialog to select a file
    private void btOpenFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOpenFileActionPerformed
        
        // Allow user to choose a file to open
        JFileChooser chooser = new JFileChooser();
        int choice = chooser.showOpenDialog(this);
        if(choice == JFileChooser.APPROVE_OPTION)
        {
            // get the file selected by user
            File file = chooser.getSelectedFile();
            readFile(file);
        }
        
        // Display results from file to form on decode tab
        lblEncodeKey.setText("Key for Encoding: " + encodeKey);
        txtAreaEncodeMsg.setText(encodedMsg);
    }//GEN-LAST:event_btOpenFileActionPerformed

