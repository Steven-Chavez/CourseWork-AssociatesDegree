    // opens save dialog allowing the user to select a file to save to
    private void createFilePath()
    {
        JFileChooser chooser = new JFileChooser();
        int choice = chooser.showSaveDialog(this);
        if(choice == JFileChooser.APPROVE_OPTION)
        {
            File file = chooser.getSelectedFile();
            writeToFile(file);
        }
    }
    
    // writes the encoded message and key to file selected by user
    private void writeToFile(File file)
    {
        try
        {FileWriter fstream = new FileWriter(file);
            BufferedWriter out = new BufferedWriter(fstream);
            out.write(enigma.getEncodedMessage());
            out.write("\r\n");
            out.write(Integer.toString(enigma.getKey()));
            
            out.close(); 
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Error:" + ex.getMessage());
            return;
        }
                
    }
    
    // reads the encoded message from the file selected
    private void readFile(File file)
    {
        try
        {
            Scanner input = new Scanner(file);
            encodedMsg = input.nextLine();
            encodeKey = input.nextInt();
        }
        catch(FileNotFoundException fne)
        {
            JOptionPane.showMessageDialog(null, "Error:" + fne.getMessage());
            return;
        }    
    }

